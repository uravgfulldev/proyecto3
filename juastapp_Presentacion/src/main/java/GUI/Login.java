    private boolean validarCampos(){
        String correo=this.txtBoxCorreo.getText();
        String password=this.txtBoxPassword.getText();
        StringBuffer mensaje = new StringBuffer();
        Pattern patronCorreo = Pattern
                .compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                        + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        if (correo.isEmpty()) {
            mensaje.append("Correo vacio\n");
        }
        
        if (password.isEmpty()) {
            mensaje.append("Contrase침a vacia\n");
        }
        
        Matcher mather=patronCorreo.matcher(correo);
        if (mather.find()==false) {
            mensaje.append("Correo invalido\n");
        }
        
        if (password.length() < 8) {
            mensaje.append("Contrase침a debe contener mas o igual a 8 caracteres\n");
        }
        
        //Validar existencia 
        if(!existsCorreo(correo)){
            mensaje.append("Correo es incorrecto\n");
        }
        
        if(!validarPassword(correo)){
            mensaje.append("Contrase침a incorrecta");
        }
            
        if (mensaje.length() == 0) {
            return true;
        }
        
         
        JOptionPane.showMessageDialog(this, mensaje, "Informaci칩n invalida", JOptionPane.INFORMATION_MESSAGE);
        return false;
    }
      
      public boolean existsCorreo(String correo){
        Usuario usuario=UsuarioDAO.consultar(correo);
        if(usuario!= null){
            System.out.println("Not Null");
            System.out.println(usuario);
            return true;
        }else{
            System.out.println("Null");
            System.out.println(usuario);
            return false;
        }
    }
       
    private boolean validarPassword(String correo){
        Usuario usuario=UsuarioDAO.consultar(correo);
        if(usuario!= null&&usuario.getPassword().equals(this.txtBoxPassword.getText())){
            System.out.println("Not Null");
            System.out.println(usuario);
            return true;
        }else{
            System.out.println("Null");
            System.out.println(usuario);
            return false;
        }
    }
